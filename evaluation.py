# Sample compute_snr function (replace with your preferred logic)
def compute_snr(raw_wav_path, clean_wav_path):
    raw, _ = sf.read(raw_wav_path)
    clean, _ = sf.read(clean_wav_path)

    # Convert stereo to mono
    if raw.ndim > 1:
        raw = raw.mean(axis=1)
    if clean.ndim > 1:
        clean = clean.mean(axis=1)

    L = min(len(raw), len(clean))
    raw, clean = raw[:L], clean[:L]
    noise = raw - clean
    snr = 10 * np.log10((np.sum(clean**2) + 1e-12) / (np.sum(noise**2) + 1e-12))
    return float(snr)


