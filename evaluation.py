# List all MP3 files
raw_blobs = [path for path in fs.ls(RAW_PREFIX) if path.lower().endswith(".mp3")]

def calculate_seg_snr_from_files(raw_path, clean_path, sr=16000, frame_ms=30, hop_ms=15):
    y_raw, _ = sf.read(raw_path)
    y_clean, _ = sf.read(clean_path)

    # Length match
    L = min(len(y_raw), len(y_clean))
    y_raw, y_clean = y_raw[:L], y_clean[:L]

    if y_raw.ndim > 1:
        y_raw = y_raw.mean(axis=1)
    if y_clean.ndim > 1:
        y_clean = y_clean.mean(axis=1)

    frame_len = int(frame_ms * sr / 1000)
    hop_len = int(hop_ms * sr / 1000)
    seg_snr = []

    for start in range(0, L - frame_len + 1, hop_len):
        seg_r = y_raw[start:start + frame_len]
        seg_c = y_clean[start:start + frame_len]
        noise = seg_r - seg_c
        snr = 10 * np.log10((np.sum(seg_c**2) + 1e-12) / (np.sum(noise**2) + 1e-12))
        seg_snr.append(snr)

    return float(np.mean(seg_snr)) if seg_snr else None

def process_in_batches(batch_size=1):  # Start with batch_size=1
    scores = []
    results = []

    for i in range(0, len(raw_blobs), batch_size):
        batch = raw_blobs[i:i + batch_size]
        for raw_blob in batch:
            try:
                base = os.path.basename(raw_blob).split('.')[0]
                base = "".join(c for c in base if c.isalnum() or c in "._- ")

                clean_blob = f"{CLEAN_PREFIX}final_{base}.mp3"
                if not fs.exists(clean_blob):
                    print(f"Skipping missing cleaned file: {clean_blob}")
                    continue

                # Optional: Log file size
                # print(f"{base}.mp3 size: {fs.info(raw_blob)['size'] / (1024 ** 2):.2f} MB")

                with tempfile.TemporaryDirectory() as td:
                    raw_local = os.path.join(td, f"{base}.mp3")
                    clean_local = os.path.join(td, f"final_{base}.mp3")
                    fs.get(raw_blob, raw_local)
                    fs.get(clean_blob, clean_local)

                    # Convert to WAV
                    raw_wav = os.path.join(td, "raw.wav")
                    clean_wav = os.path.join(td, "clean.wav")
                    data_r, sr_r = sf.read(raw_local)
                    data_c, sr_c = sf.read(clean_local)
                    sf.write(raw_wav, data_r, sr_r)
                    sf.write(clean_wav, data_c, sr_c)

                    global_snr = compute_snr(raw_wav, clean_wav)
                    seg_snr = calculate_seg_snr_from_files(raw_wav, clean_wav, sr=sr_r)

                    if seg_snr is not None:
                        scores.append(seg_snr)
                        results.append([base + ".mp3", global_snr, seg_snr])
                        print(f"Processed {base}.mp3: segmental SNR = {seg_snr:.2f} dB, Global SNR = {global_snr:.2f} dB")

                # Cleanup
                gc.collect()

            except Exception as e:
                print(f"Error processing {base}.mp3: {e}")
                continue

    if scores:
        print(f"\nAverage segmental SNR over {len(scores)} files: {np.mean(scores):.2f} dB")
        print("\nEvaluation Results:")
        print(tabulate(results, headers=["Audio Title", "Global SNR (dB)", "Segmental SNR (dB)"]))
    else:
        print("No valid SNR scores computed.")

# Run
process_in_batches(batch_size=1)